function aplicarTraduccion(idioma) {
  const traducciones = textos[idioma];

  // Textos normales
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const clave = el.getAttribute("data-i18n");
    if (traducciones[clave]) {
      if (clave.includes("titulo-hero")) {
        el.innerHTML = traducciones[clave]; // permite HTML
      } else {
        el.textContent = traducciones[clave];
      }
    }
  });

  // Placeholder de formularios
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const clave = el.getAttribute("data-i18n-placeholder");
    if (traducciones[clave]) {
      el.setAttribute("placeholder", traducciones[clave]);
    }
  });
}

function configurarIdioma() {
  const idiomaGuardado = localStorage.getItem("idiomaSeleccionado") || "es";
  aplicarTraduccion(idiomaGuardado);

  const selector = document.getElementById("selector-idioma");
  if (selector) {
    selector.value = idiomaGuardado;
    selector.addEventListener("change", () => {
      const nuevoIdioma = selector.value;
      localStorage.setItem("idiomaSeleccionado", nuevoIdioma);
      aplicarTraduccion(nuevoIdioma);
    });
  }
}

document.addEventListener("DOMContentLoaded", configurarIdioma);
